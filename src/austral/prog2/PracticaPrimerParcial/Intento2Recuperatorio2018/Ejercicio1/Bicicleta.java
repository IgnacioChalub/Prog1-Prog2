package austral.prog2.PracticaPrimerParcial.Intento2Recuperatorio2018.Ejercicio1;

public class Bicicleta {

    String modelo;
    int año;

    public Bicicleta(String modelo, int año) {
        this.modelo = modelo;
        this.año = año;
    }

    public String getModelo() {
        return modelo;
    }

    public int getAño() {
        return año;
    }
}
