package austral.prog2.PracticaPrimerParcial.Recuperatorio2018.Ejercicio1;

public class Bicicleta {

    String marca;
    int año;

    public Bicicleta(String marca, int año) {
        this.marca = marca;
        this.año = año;
    }

    public String getMarca() {
        return marca;
    }

    public int getAño() {
        return año;
    }
}
