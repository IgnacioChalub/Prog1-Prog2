package austral.prog1.tp1;

public class Tp1IgnacioChalubMaths {
    public static void main(String[] args) {
        int x;
        int y;

        float z;
        float w;

        x=10;
        y=6;

        z=10f;
        w=6f;

        System.out.println( x + y );
        System.out.println( x - y );
        System.out.println( x * y );
        System.out.println( (x + y) / 2 );
        System.out.println( x / y );
        System.out.println( x % y );
        System.out.println( z / w );
    }
}
